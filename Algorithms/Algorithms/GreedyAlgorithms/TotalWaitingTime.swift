import Foundation

func minimumWaitingTime(_ queries: inout [Int]) -> Int {
    var queriesIdx = 0
    let queriesCount = queries[queriesIdx]
    var counter = 0
    
    for i in 0 ..< queries.count {
        counter = queriesCount + queriesCount
    }
    
    return counter
}
